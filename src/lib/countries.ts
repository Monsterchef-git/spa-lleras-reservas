/** Convierte ISO alpha-2 ("US") a emoji bandera ("🇺🇸"). */
export function flagEmoji(code: string): string {
  if (!code || code.length !== 2) return "🌐";
  const upper = code.toUpperCase();
  // Cada letra A-Z → Regional Indicator Symbol (U+1F1E6 a U+1F1FF)
  return String.fromCodePoint(
    ...upper.split("").map((c) => 0x1f1e6 + c.charCodeAt(0) - 65),
  );
}

/** Normaliza para búsqueda accent-insensitive. */
function norm(s: string) {
  return s.normalize("NFD").replace(/[\u0300-\u036f]/g, "").toLowerCase();
}

/** Busca un país por nombre exacto (case + accent insensitive). */
export function findCountryByName(name: string | null | undefined): Country | null {
  if (!name) return null;
  const n = norm(name);
  return COUNTRIES.find((c) => norm(c.name) === n) ?? null;
}
